// 朝のあいさつを出力する関数を作成する
const sayGoodMorning = () => {
  console.log('おはようございます！');
  console.log('昨日はよく眠れましたか？');
  console.log('今日も一日頑張りましょう！');
}

// 夜のあいさつを出力する関数を定義する
const sayGoodEvening = () => {
 console.log('こんばんは！');
 console.log('今日も一日お疲れさまでした。');
}

console.log('■ 朝のあいさつを出力する関数を呼び出す');
sayGoodMorning();

console.log('■ 夜のあいさつを出力する関数を呼び出す');
sayGoodEvening();